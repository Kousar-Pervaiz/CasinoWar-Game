// compares the user's guess with the ouput gotten from dice rolls
void dice_check(int &dice_one, int &dice_two, int &guess_dice, float &balance, float &bet_amount)
{

    int check;

    if ((dice_one % 2 == 0) & (dice_two % 2 == 0)) // if both the dice display even numbers
    {
        check = 1;
    }
    else if ((dice_one % 2 == 1) & (dice_two % 2 == 1)) // if both the dice display odd numbers
    {
        check = 2;
    }
    else if ((dice_one % 2 == 1) & (dice_two % 2 == 0) || (dice_one % 2 == 0) & (dice_two % 2 == 1)) // one dice has odd and other has even
    {
        check = 3;
    }

    if (check == guess_dice) // if correct guess
    {

        balance = (balance - bet_amount) + (bet_amount * 10);
        cout << "\t\t\t\t\t\t\t\t\t Congratulations! You won..." << endl;
        cout << "\t\t\t\t\t\t\t\t\t Your Current Balance: " << balance << endl;
    }
    else // if wrong guess
    {
        balance = balance - bet_amount;
        cout << "\t\t\t\t\t\t\t\t\t  Oops! You lose...  :(" << endl;
        cout << "\t\t\t\t\t\t\t\t\t  Your Current Balance: " << balance << endl;
    }
}

